package com.example.labexercise4;

public class Trapezoid extends Shape {
    private int LeftBase, RightBase, LeftTop, RightTop;
    public Trapezoid(int LeftBase, int RightBase, int LeftTop, int RightTop) {
        this.LeftBase = LeftBase;
        this.LeftTop = LeftTop;
        this.RightBase = RightBase;
        this.RightTop = RightTop;
    }

}
